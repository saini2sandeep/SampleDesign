        setContentView(R.layout.activity_expend_list);


        // get the listview
        expListView = (ExpandableListView) findViewById(R.id.lvExp);

        // preparing list data
        prepareListData();

        listAdapter = new com.example.sandeepsaini.multitaskapp.ExpandableListAdapter(this, listDataHeader, listDataChild);

